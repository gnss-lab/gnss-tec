def test_validity():
    tec = Tec(
        datetime(2016, 1, 1),
        'GPS',
        'G01',
    )

    tec.phase = {
        1: 134680873.916,
        2: 104946140.779
    }

    tec.p_range = {
        1: 25475915.229,
        2: 0
    }

    tec.lli = {
        1: 0,
        2: 1
    }

    validity = 2 ** 4 + 2 ** 2

    assert validity == tec.validity


